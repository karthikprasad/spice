
public class MatrixContainer {

	private double[][] forwardMatrix;
	private double[][] backwardMatrix;

	public double[][] getForwardMatrix() {
		return forwardMatrix;
	}
	public void setForwardMatrix(double[][] forwardMatrix) {
		this.forwardMatrix = forwardMatrix;
	}
	public double[][] getBackwardMatrix() {
		return backwardMatrix;
	}
	public void setBackwardMatrix(double[][] backwardMatrix) {
		this.backwardMatrix = backwardMatrix;
	}
	
}
